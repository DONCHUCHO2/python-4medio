numeros = [1,2,3,4,5]
#pedimos el valor del primer elemento
print(numeros[0])
#tambien podemos hacerlo de forma negativa
print(numeros[-1])
#si quieres esoger datos de 2 en 2 hacemos lo siguiente
print(numeros[0:4:2])
#para juntar dos listas podemos sumarlas de la siguiente forma
